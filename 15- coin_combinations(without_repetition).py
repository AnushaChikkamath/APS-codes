from math import *
import sys

def coin_combinations_2(denominations,value):
  dp=([0 for i in range(value+1)])

  dp[0]=1
  for i in range(len(denominations)):
    for j in range(1,value+1):
      if(j-denominations[i]>=0):
        dp[j]=dp[j]+dp[j-denominations[i]]

  return dp[value]
  

def main():
  print('Enter the denominations array')
  denominations = list(map(int, input().split()))
  sum_value=int(input('Enter the sum value'))
  number_ways=coin_combinations_2(denominations,sum_value)
  print(f'The number of possible combinations of sum {sum_value} from the given denominations without repetion is {number_ways}.')


if __name__=="__main__":
  main()

